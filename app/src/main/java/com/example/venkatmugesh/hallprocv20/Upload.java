package com.example.venkatmugesh.hallprocv20;

public class Upload {

    private double X;
    private double Y;
    private double Z;

    public Upload(double x, double y, double z) {
        X = x;
        Y = y;
        Z = z;
    }

    public Upload() {
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getY() {
        return Y;
    }

    public void setY(double y) {
        Y = y;
    }

    public double getZ() {
        return Z;
    }

    public void setZ(double z) {
        Z = z;
    }
}
